# For Loops

# used when the number of iterations is known
# used when looping over a sequence
# The loop variable takes on the values of the elements in the sequence

numbers = [1, 2, 3, 4, 5]
for notAlphabets in numbers:
    print(notAlphabets)

# we can iterate strings directly using for loop

string_value = "Greetings"
for letter in string_value:
    print(letter)

"""
"break" will come out of the loop
"continue" will stop the current iteration and jump to next iteration
"""
